package com.briup.bean;

public class Record {

    private String date;
    private String shouzhi;
    private String qianbao;
    private String type;
    private float money;
    private String beizhu;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getShouzhi() {
        return shouzhi;
    }

    public void setShouzhi(String shouzhi) {
        this.shouzhi = shouzhi;
    }

    public String getQianbao() {
        return qianbao;
    }

    public void setQianbao(String qianbao) {
        this.qianbao = qianbao;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public float getMoney() {
        return money;
    }

    public void setMoney(float money) {
        this.money = money;
    }

    public String getBeizhu() {
        return beizhu;
    }

    public void setBeizhu(String beizhu) {
        this.beizhu = beizhu;
    }
}
